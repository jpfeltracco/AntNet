Map::Map() {
    water = TextureManager::load_texture("water.png");
    grass = TextureManager::load_texture("grass.png");
    dirt = TextureManager::load_texture("dirt.png");

    load_map(lvl1);

    src.x = 0;
    src.y = 0;
    src.w = 32;
    src.h = 32;

    dest.x = 0;
    dest.y = 0;
    dest.w = 32;
    dest.h = 32;
}

void Map::load_map(int arr[20][25]) {
    for (std::size_t row = 0; row < 20; ++row) {
        for (std::size_t col = 0; col < 25; ++col) {
            map[row][col] = arr[row][col];
        }
    }
}

void Map::draw_map() {
    for (int row = 0; row < 20; ++row) {
        for (int col = 0; col < 25; ++col) {

            dest.x = col*dest.w;
            dest.y = row*dest.h;

            switch (map[row][col]) {
                case 0:
                    TextureManager::draw(water, src, dest);
                    break;
                case 1:
                    TextureManager::draw(grass, src, dest);
                    break;
                case 2:
                    TextureManager::draw(dirt, src, dest);
                    break;
                default:
                    break;
            }
        }
    }
}
